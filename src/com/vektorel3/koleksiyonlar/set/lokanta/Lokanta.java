package com.vektorel3.koleksiyonlar.set.lokanta;

public class Lokanta {
	String isim;
	String adres;
	
	public Lokanta(String isim,String adres) {
		this.isim = isim;
		this.adres = adres;
	}
	
	public String getAdres() {
		return adres;
	}
	
	public String getIsim() {
		return isim;
	}
	

}
