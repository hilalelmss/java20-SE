package com.vektorel3.koleksiyonlar.list.insaat;

public class InsaatFirmasi {
	int id;
	String name;
	
	public InsaatFirmasi(int id, String name) {
		this.id = id;
		this.name = name;
	}
	
	public int getId() {
		return id;
	}
	
	public String getName() {
		return name;
	}
	

}
