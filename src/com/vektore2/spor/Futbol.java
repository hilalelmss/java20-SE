package com.vektore2.spor;

public class Futbol {
	
	private int oyuncuSayisi;
	private int hakemSayisi;
	private int topSayisi;
	private int yedekOyuncu;
	
	public int getHakemSayisi() {
		return hakemSayisi;
	}
	
	public int getOyuncuSayisi() {
		return oyuncuSayisi;
	}
	
	public int getTopSayisi() {
		return topSayisi;
	}
	
	public int getYedekOyuncu() {
		return yedekOyuncu;
	}
	
	public void setHakemSayisi(int hakemSayisi) {
		this.hakemSayisi = hakemSayisi;
	}
	
	public void setOyuncuSayisi(int oyuncuSayisi) {
		this.oyuncuSayisi = oyuncuSayisi;
	}
	
	public void setTopSayisi(int topSayisi) {
		this.topSayisi = topSayisi;
	}
	
	public void setYedekOyuncu(int yedekOyuncu) {
		this.yedekOyuncu = yedekOyuncu;
	}
}
